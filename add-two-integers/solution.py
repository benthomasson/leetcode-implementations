class Solution:
    def sum(self, num1: int, num2: int) -> int:
        """Return the sum of two integers.

        Args:
            num1: First integer (-100 <= num1 <= 100).
            num2: Second integer (-100 <= num2 <= 100).

        Returns:
            The sum of num1 and num2.
        """
        return num1 + num2
