class Solution:
    def checkIfBinaryStringHasAtMostOneSegmentOfOnes(self, s: str) -> bool:
        """Check if binary string has at most one contiguous segment of ones.

        Args:
            s: Binary string without leading zeros.

        Returns:
            True if at most one contiguous segment of ones exists.
        """
        return "01" not in s


